    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        // TODO add your handling code here:
        String roll=jTextField1.getText();
        String physics=jTextField2.getText();
        String maths=jTextField4.getText();
        String em=jTextField3.getText();
        String dbms=jTextField5.getText();
        String os=jTextField6.getText();
        
            try{
           Class.forName("com.mysql.cj.jdbc.Driver");
           Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/srm" ,"root","Moz@0786");
           Statement st=con.createStatement();
            ResultSet rs=st.executeQuery("select * from student where roll='"+roll+"'");
            if(rs.next())
            {
                st.executeUpdate("insert into result(rollno,physics,maths,em,dbms,os) values('"+roll+"','"+physics+"','"+maths+"','"+em+"','"+dbms+"','"+os+"')");
                JOptionPane.showMessageDialog(null, "result is saved successfully");
                setVisible(false);
                new insertresult().setVisible(true);
            }
            else
            {
                JOptionPane.showMessageDialog(null, "student is not registred");
            }
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null, e.toString());
        } 
    }//GEN-LAST:event_jButton6ActionPerformed

